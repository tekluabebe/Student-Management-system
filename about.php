					<p>Bayero University Kano (BUK) is a university situated in Kano, Kano State, Nigeria. It was founded in 1975, when it was renamed from Bayero University College and upgraded from university college to university. It is the first university in Kano State. </p>Bayero University initially bore the name Ahmadu Bello College, the college was named after Premier of Northern Nigeria, Ahmadu Bello. It was founded in January 1960 by the Northern Nigeria Ministry of Education headed by Isa Kaita to prepare senior secondary certificate holders for G.C.E. and A-level in Arabic, Hausa, Islamic History, Islamic studies and English Literature.[2] Upon the establishment of Ahmadu Bello University in Zaria, Kaduna State, Ahmadu Bello College was renamed to Abdullahi Bayero College, after Abdullahi Bayero, Emir of Kano[3] and soon thereafter it became a faculty of the new university.
							Initially located on the grounds of the School of Arabic Studies near the palace of the Emir, the college moved to a location at the old Kano Airport Hotel, where it remained until March 1968, when it moved to Western Kano to make way for a military hospital (the Nigerian Civil War having started the previous year). The first students began their studies in February 1964, and they graduated in July 1966.
							In 1975, the college became a university college, and was renamed Bayero University College, with Mahmud Tukur as principal. In 1977, the status was again changed, to that of university, and Bayero University became the name; Tukur became vice-chancellor. In 1980 the university ceased functioning as a faculty of its Zaria counterpart.
							
